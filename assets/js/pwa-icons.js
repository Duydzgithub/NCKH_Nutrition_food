// Generate icon files from Data URLs
function createIconFiles() {
    const icons = [
        { size: 72, dataUrl: FoodNinjaIcons.icon72 },
        { size: 96, dataUrl: FoodNinjaIcons.icon96 },
        { size: 128, dataUrl: FoodNinjaIcons.icon128 },
        { size: 144, dataUrl: FoodNinjaIcons.icon144 },
        { size: 152, dataUrl: FoodNinjaIcons.icon152 },
        { size: 192, dataUrl: FoodNinjaIcons.icon192 },
        { size: 384, dataUrl: FoodNinjaIcons.icon384 },
        { size: 512, dataUrl: FoodNinjaIcons.icon512 }
    ];

    icons.forEach(icon => {
        const link = document.createElement('a');
        link.download = `icon-${icon.size}x${icon.size}.png`;
        link.href = icon.dataUrl;
        document.body.appendChild(link);
        link.click();
        document.body.removeChild(link);
    });

    console.log('✅ PWA icons generated successfully!');
}

// Convert SVG Data URL to PNG (using Canvas)
function convertSVGtoPNG(svgDataUrl, size, callback) {
    const img = new Image();
    img.onload = function() {
        const canvas = document.createElement('canvas');
        const ctx = canvas.getContext('2d');
        canvas.width = size;
        canvas.height = size;
        
        ctx.drawImage(img, 0, 0, size, size);
        
        canvas.toBlob(blob => {
            const url = URL.createObjectURL(blob);
            callback(url);
        }, 'image/png');
    };
    img.src = svgDataUrl;
}

